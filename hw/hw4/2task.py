# Входные данные: список урожайности кустов черники
arr = [4, 2, 3, 5, 1, 6]

# Определяем количество кустов
n = len(arr)

# Если кустов меньше трех, то собирающий модуль не может собрать ягоды с трех кустов.
if n < 3:
    max_berries = sum(arr)  # Просто суммируем все кусты
else:
    max_berries = 0
    for i in range(n):
        # Сумма ягод с текущего куста и двух соседних (с учётом цикличности)
        current_sum = arr[i] + arr[(i - 1) % n] + arr[(i + 1) % n]
        # Обновляем максимальное значение, если текущая сумма больше
        if current_sum > max_berries:
            max_berries = current_sum

# Выводим результат
print(max_berries)
